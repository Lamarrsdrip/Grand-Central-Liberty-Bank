// Locales not in the full translation set fall back to English
const FALLBACK = translations.en as TranslationDict;

export function getTranslations(locale: SupportedLocale): TranslationDict {
  const dict = translations[locale as string];
  if (!dict) return FALLBACK;
  // Fill any gaps with English fallbacks
  return new Proxy(dict, {
    get(target, key) {
      return (target as Record<string, string>)[key as string] ?? (FALLBACK as Record<string, string>)[key as string] ?? key;
    },
  }) as TranslationDict;
}

export function t(locale: SupportedLocale, key: TranslationKey): string {
  const dict = getTranslations(locale);
  return dict[key] ?? FALLBACK[key] ?? key;
}
